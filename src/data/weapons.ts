const normalizeWeaponToken = (value: string): string =>
  value.trim().toLowerCase().replace(/\s+/g, "-");

export const resolveWeaponId = (
  value: string | WeaponId | null | undefined,
  fallback: WeaponId = "assault-rifle"
): WeaponId => {
  if (!value) {
    return fallback;
  }

  if (value in WEAPONS) {
    return value as WeaponId;
  }

  const normalized = normalizeWeaponToken(value);
  if (normalized in WEAPONS) {
    return normalized as WeaponId;
  }

  return LEGACY_WEAPON_ALIASES[normalized] ?? fallback;
};
